    private void logInSelectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logInSelectButtonActionPerformed
        // TODO add your handling code here:

        if (nameTextField.getText().equals("") || passTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "All Fields Are Required!");
        } else {
            jProgressBar1.setValue(0);
            jProgressBar1.setVisible(true);
            con = MySqlConnect.ConnectDB();
            try {
                PreparedStatement pst = con.prepareStatement("SELECT *FROM user_data where username=? and password=? ");
                pst.setString(1, nameTextField.getText());
                pst.setString(2, passTextField.getText());

                rs = pst.executeQuery();

                if (rs.next()) {
                    if (rs.getString("access").equalsIgnoreCase("Admin")) {
                        isAdmin = true;
                        showProgress();
                    } else {
                        isUser = true;
                        showProgress();
                    }
                } else {
                    isFailed = true;
                    showProgress();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Failed to Log In", "Try Again!", JOptionPane.ERROR_MESSAGE);
            }
            //  JOptionPane.showMessageDialog(null, "Log In Successful!");
        }
    }//GEN-LAST:event_logInSelectButtonActionPerformed

