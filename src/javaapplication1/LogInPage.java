    public void showProgress() {
        Timer t = new Timer(50, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                jProgressBar1.setValue(jProgressBar1.getValue() + 2);
                if (jProgressBar1.getValue() == 100) {
                    ((Timer) e.getSource()).stop();
                    jProgressBar1.setVisible(false);
                    if (isAdmin) {
                        LogInPage.this.setVisible(false);
                        AdminUI aui = new AdminUI();
                        aui.setVisible(true);
                        JOptionPane.showMessageDialog(null, "Welcome Administrator");
                    } else if (isUser) {
                        LogInPage.this.setVisible(false);
                        EmployeeUI eui = new EmployeeUI();
                        eui.setVisible(true);
                        JOptionPane.showMessageDialog(null, "Welcome User");
                    } else {
                        JOptionPane.showMessageDialog(null, "user name or password didn't match!", "Try Again", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        });
        t.start();
    }

